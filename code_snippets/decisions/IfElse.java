
import java.io.*;
import java.util.*;

class IfElse
{
    public static void main(String[] args)
    {
        Scanner kbd = new Scanner(System.in);
        System.out.print("Enter an integer: ");
        int anInt = kdb.nextInt();
        if(anInt == 1)
        {
            System.out.println("You entered... 1!");
        }
        else
        {
            System.out.println("You entered... not 1!");
        }
        System.out.println("Outside of if-else, always print");
    }

}
